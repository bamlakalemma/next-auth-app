'use client';

import { useState, FormEvent } from 'react';
import { useRouter } from 'next/navigation';
import Link from 'next/link';
import { signup } from '@/lib/api';
import { validateSignupForm, type ValidationError } from '@/lib/validation';

// Google Logo SVG Component
const GoogleLogo = () => (
  <svg className="w-5 h-5" viewBox="0 0 24 24">
    <path
      fill="#4285F4"
      d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
    />
    <path
      fill="#34A853"
      d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
    />
    <path
      fill="#FBBC05"
      d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
    />
    <path
      fill="#EA4335"
      d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
    />
  </svg>
);

export default function SignupPage() {
  const router = useRouter();
  const [formData, setFormData] = useState({
    name: '',
    email: '',
    password: '',
    confirmPassword: '',
    role: 'user', // Hidden field for API requirement
  });
  const [errors, setErrors] = useState<ValidationError[]>([]);
  const [isLoading, setIsLoading] = useState(false);
  const [apiError, setApiError] = useState<string | null>(null);
  const [success, setSuccess] = useState(false);

  const handleChange = (e: React.ChangeEvent<HTMLInputElement | HTMLSelectElement>) => {
    const { name, value } = e.target;
    setFormData((prev) => ({ ...prev, [name]: value }));
    // Clear field-specific error when user starts typing
    setErrors((prev) => prev.filter((err) => err.field !== name));
    setApiError(null);
  };

  const handleSubmit = async (e: FormEvent<HTMLFormElement>) => {
    e.preventDefault();
    setApiError(null);
    setSuccess(false);

    // Client-side validation
    const validationErrors = validateSignupForm(formData);
    if (validationErrors.length > 0) {
      setErrors(validationErrors);
      return;
    }

    setIsLoading(true);

    try {
      const response = await signup(formData);

      if (response.success) {
        setSuccess(true);
        // Redirect to email verification page after 2 seconds
        setTimeout(() => {
          router.push(`/verify-email?email=${encodeURIComponent(formData.email)}`);
        }, 2000);
      } else {
        setApiError(response.error || 'Signup failed. Please try again.');
      }
    } catch (error) {
      setApiError('An unexpected error occurred. Please try again.');
    } finally {
      setIsLoading(false);
    }
  };

  const handleGoogleSignup = () => {
    // Placeholder for Google signup functionality
    // This would typically integrate with Google OAuth
    alert('Google signup functionality would be implemented here');
  };

  const getFieldError = (fieldName: string): string | undefined => {
    return errors.find((err) => err.field === fieldName)?.message;
  };

  return (
    <div className="flex min-h-screen items-center justify-center px-4 py-12">
      <div className="w-full max-w-md">
        <div className="bg-white rounded-lg p-8">
          {/* Header */}
          <h1 className="text-center text-[#25324B] mb-8 font-[var(--font-poppins)] font-black text-[32px] leading-[120%] tracking-[0%]">
            Sign Up Today!
          </h1>

          {/* Success Message */}
          {success && (
            <div className="mb-4 rounded-lg bg-green-50 p-4 text-sm text-green-800">
              Account created successfully! Redirecting to email verification...
            </div>
          )}

          {/* Error Message */}
          {apiError && (
            <div className="mb-4 rounded-lg bg-red-50 p-4 text-sm text-red-800">
              {apiError}
            </div>
          )}

          {/* Google Sign Up Button */}
          <button
            type="button"
            onClick={handleGoogleSignup}
            className="w-full flex items-center justify-center gap-3 border border-[#CCCCF5] rounded-lg px-4 py-3 bg-white text-blue-900 font-medium mb-6"
          >
            <GoogleLogo />
            <span className="font-[var(--font-epilogue)] font-semibold leading-[160%] tracking-[0%] text-center text-[#3833B2]">
              Sign Up with Google
            </span>
          </button>

          {/* Separator */}
          <div className="relative mb-6">
            <div className="absolute inset-0 flex items-center">
              <div className="w-full border-t border-gray-300"></div>
            </div>
            <div className="relative flex justify-center text-sm">
              <span className="px-2 bg-white font-[var(--font-epilogue)] text-normal text-base leading-[160%] tracking-[0%] text-center text-[#202430]/50">Or Sign Up with Email</span>
            </div>
          </div>

          {/* Email Sign Up Form */}
          <form onSubmit={handleSubmit} className="space-y-5">
            {/* Full Name */}
            <div>
              <label htmlFor="name" className="block text-sm font-semibold text-[#515B6F] mb-1">
                Full Name
              </label>
              <input
                type="text"
                id="name"
                name="name"
                value={formData.name}
                onChange={handleChange}
                className={`w-full rounded-lg border px-4 py-3 transition-colors placeholder:text-[#A8ADB7] ${
                  getFieldError('name')
                    ? 'border-red-300 bg-red-50 focus:border-red-500 focus:ring-red-500'
                    : 'border-gray-300 bg-white focus:border-blue-500 focus:ring-blue-500'
                } focus:outline-none focus:ring-1`}
                placeholder="Enter your full name"
                disabled={isLoading}
              />
              {getFieldError('name') && (
                <p className="mt-1 text-sm text-red-600">{getFieldError('name')}</p>
              )}
            </div>

            {/* Email Address */}
            <div>
              <label htmlFor="email" className="block text-sm font-semibold  text-[#515B6F] mb-1">
                Email Address
              </label>
              <input
                type="email"
                id="email"
                name="email"
                value={formData.email}
                onChange={handleChange}
                className={`w-full rounded-lg border px-4 py-3 transition-colors placeholder:text-[#A8ADB7] ${
                  getFieldError('email')
                    ? 'border-red-300 bg-red-50 focus:border-red-500 focus:ring-red-500'
                    : 'border-gray-300 bg-white focus:border-blue-500 focus:ring-blue-500'
                } focus:outline-none focus:ring-1`}
                placeholder="Enter email address"
                disabled={isLoading}
              />
              {getFieldError('email') && (
                <p className="mt-1 text-sm text-red-600">{getFieldError('email')}</p>
              )}
            </div>

            {/* Password */}
            <div>
              <label htmlFor="password" className="block text-sm font-semibold  text-[#515B6F] mb-1">
                Password
              </label>
              <input
                type="password"
                id="password"
                name="password"
                value={formData.password}
                onChange={handleChange}
                className={`w-full rounded-lg border px-4 py-3 transition-colors placeholder:text-[#A8ADB7] ${
                  getFieldError('password')
                    ? 'border-red-300 bg-red-50 focus:border-red-500 focus:ring-red-500'
                    : 'border-gray-300 bg-white focus:border-blue-500 focus:ring-blue-500'
                } focus:outline-none focus:ring-1`}
                placeholder="Enter password"
                disabled={isLoading}
              />
              {getFieldError('password') && (
                <p className="mt-1 text-sm text-red-600">{getFieldError('password')}</p>
              )}
            </div>

            {/* Confirm Password */}
            <div>
              <label htmlFor="confirmPassword" className="block text-sm font-semibold  text-[#515B6F] mb-1">
                Confirm Password
              </label>
              <input
                type="password"
                id="confirmPassword"
                name="confirmPassword"
                value={formData.confirmPassword}
                onChange={handleChange}
                className={`w-full rounded-lg border px-4 py-3 transition-colors placeholder:text-[#A8ADB7] ${
                  getFieldError('confirmPassword')
                    ? 'border-red-300 bg-red-50 focus:border-red-500 focus:ring-red-500'
                    : 'border-gray-300 bg-white focus:border-blue-500 focus:ring-blue-500'
                } focus:outline-none focus:ring-1`}
                placeholder="Enter password"
                disabled={isLoading}
              />
              {getFieldError('confirmPassword') && (
                <p className="mt-1 text-sm text-red-600">{getFieldError('confirmPassword')}</p>
              )}
            </div>

            {/* Hidden Role Field (required by API) */}
            <input type="hidden" name="role" value={formData.role} />

            {/* Continue Button */}
            <button
              type="submit"
              disabled={isLoading}
              className="w-full rounded-full bg-[#2D298E] px-4 py-3 font-semibold text-white disabled:cursor-not-allowed disabled:opacity-50"
            >
              {isLoading ? 'Creating Account...' : 'Continue'}
            </button>
          </form>

          {/* Login Link */}
          <p className="mt-6 text-start text-[14px] font-[var(--font-epilogue)] text-normal leading-[160%] tracking-[0%] text-[#202430]/70">
            Already have an account?{' '}
            <Link
              href="/signin"
              className="font-medium text-[#2D298E] "
            >
              Login
            </Link>
          </p>

          {/* Terms and Privacy Policy */}
          <p className="mt-6 text-start font-[var(--font-epilogue)] text-[14px] leading-[160%] tracking-[0%] text-[#7C8493]">
            By clicking 'Continue', you acknowledge that you have read and accepted our{' '}
            <Link href="/terms" className="text-[#2D298E] ">
              Terms of Service
            </Link>
            {' '}and{' '}
            <Link href="/privacy" className="text-[#2D298E] ">
              Privacy Policy
            </Link>
            .
          </p>
        </div>
      </div>
    </div>
  );
}
